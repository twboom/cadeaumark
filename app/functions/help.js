function help() {
  newCommandLine('-------: Markinal Help Menu :-------')
  newCommandLine('help: Shows you this screen, usage: [help]')
  newCommandLine('login: Logging in in Markinal, usage: [login <username> <password>]')
  newCommandLine('logout: Logs you out of Markinal, usage: [logout]')
  newCommandLine('clue list: Lists the clues, usage: [clue list]')
  newCommandLine('clue unlock: unlocks the clue, usage: [clue unlock <clueNumber> <quiz answer>]')
  newCommandLine('-----------------::-----------------')
}
